                    cancelLabel: 'Clear'
                }
            });

            $('input[name="drange"]').on('apply.daterangepicker', function(ev, picker) {
                $(this).val(picker.startDate.format('YYYY-MM-DD') + ' - ' + picker.endDate.format(
                    'YYYY-MM-DD'));
            });

            $('input[name="drange"]').on('cancel.daterangepicker', function(ev, picker) {
                $(this).val('');
            });

